#ler 4 notas e calcular a média entre elas
n1 = float(input('Pirmeira nota do aluno'))
n2 = float(input('Segunda nota do aluno'))
n3 = float(input('Terceira nota do aluno'))
n4 = float(input('quarta nota do aluno'))
média = (n1 + n2 + n3 + n4) / 2
print('A média entre {}, {}, {}, {} é igual a {}'.format(n1, n2, n3, n4, média))